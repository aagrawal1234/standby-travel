import { existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const tripsDirectory = path.join(root, "public", "trips");
const outputFile = path.join(root, "data", "trip-media.generated.ts");

const imageExtensions = new Set([
  ".avif",
  ".gif",
  ".jpeg",
  ".jpg",
  ".png",
  ".webp",
]);
const videoExtensions = new Set([".mp4", ".mov", ".ogg", ".webm"]);

function mediaAltFromFilename(filename) {
  return filename
    .replace(/\.[^/.]+$/, "")
    .replace(/^\d+[-_\s.]*/, "")
    .replace(/[-_]+/g, " ")
    .trim();
}

function mediaTypeFromExtension(filename) {
  const extension = path.extname(filename).toLowerCase();

  if (imageExtensions.has(extension)) {
    return "image";
  }

  if (videoExtensions.has(extension)) {
    return "video";
  }

  return null;
}

function tripMediaForSlug(slug) {
  const tripDirectory = path.join(tripsDirectory, slug);

  return readdirSync(tripDirectory, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => {
      const type = mediaTypeFromExtension(entry.name);

      if (!type) {
        return null;
      }

      const alt = mediaAltFromFilename(entry.name);

      return {
        type,
        src: `/trips/${slug}/${entry.name}`,
        alt: alt || `${slug} trip media`,
      };
    })
    .filter(Boolean)
    .sort((first, second) =>
      first.src.localeCompare(second.src, undefined, {
        numeric: true,
        sensitivity: "base",
      }),
    );
}

const tripMedia = existsSync(tripsDirectory)
  ? Object.fromEntries(
      readdirSync(tripsDirectory, { withFileTypes: true })
        .filter((entry) => entry.isDirectory())
        .map((entry) => [entry.name, tripMediaForSlug(entry.name)])
        .filter(([, media]) => media.length > 0)
        .sort(([first], [second]) => first.localeCompare(second)),
    )
  : {};

mkdirSync(path.dirname(outputFile), { recursive: true });
writeFileSync(
  outputFile,
  `// This file is generated by scripts/generate-trip-media.mjs.\n` +
    `// Run npm run generate:media after changing public/trips files.\n\n` +
    `export type GeneratedTripMedia = {\n` +
    `  type: "image" | "video";\n` +
    `  src: string;\n` +
    `  alt: string;\n` +
    `};\n\n` +
    `export const tripMedia: Record<string, GeneratedTripMedia[]> = ${JSON.stringify(tripMedia, null, 2)};\n`,
);

console.log(`generated ${path.relative(root, outputFile)}`);
